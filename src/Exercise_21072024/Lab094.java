public class Lab094 {
    public static void main(String[] args) {
        // While -> int -> condition -> body -> increment / decrement

        // Do While
        // int -> body -> condition -> incre / drement

        int i=0;
        do
            {
                System.out.println(i);
                i++;
            }while(i<=10);

        System.out.println("-----------------");

        int i1=1;
        do {
                System.out.println(i1);
                i1++;
            }while(i1>=10);
    }
}
