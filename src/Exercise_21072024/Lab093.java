package Exercise_21072024;

public class Lab093 {
    public static void main(String[] args) {
        //While loop
        // syntax - while - A, B, C
        //         // A - Init
        //        // B - Condition
        //        // C - Increment / Decrement

        int i1=1;
        while(i1<=10)
        {
            System.out.println(i1);
            i1++;
        }

        System.out.println("-------------------");

        int i2=25;
        while(i2>=1)
        {
            System.out.println(i2);
            i2--;
        }

    }
}
