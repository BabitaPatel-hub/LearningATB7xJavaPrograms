package Exercise_13072024;

public class Lab017 {
    public static void main(String[] args) {

        //Concatenation

        String firstname="Babita";
        System.out.println(firstname);

        String lastname="Patel";
        System.out.println(lastname);

        System.out.println(firstname + lastname);

        System.out.println("Babita" + " " + "Patel");

        int x = 10;
        int y = 20;
        System.out.println(x+y);
        System.out.println(firstname+x);
        System.out.println(x+firstname);
        System.out.println(x+y+"Test");
        System.out.println(x+"test"+y);
        System.out.println(x+firstname+y+x);
        System.out.println(x+firstname+y+x+y+y);

    }
}
