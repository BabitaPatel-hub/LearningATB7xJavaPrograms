package Exercise_13072024;

public class Lab012 {
    public static void main(String[] args)
    {
        // data_type variable_name = variable_value(literal)
        // Data Type
        // Primitive Data Type
        // boolean, char, byte, int, short, long, float, double
        //boolean : true, false
        boolean is_married = true;
        boolean is_rich = false;
        System.out.println(is_married);
        System.out.println(is_rich);

        //Numerical-Integral->byte,short,int,long
        //byte
        byte b1=125;
        System.out.println(b1);
        byte b2=-100;
        System.out.println(b2);
        //short
        short s=-5000;
        System.out.println(s);
        //int
        int abc=-656555656;
        System.out.println(abc);
        //long
        long l1=-9425330000l;
        long l2=9425330000L;
        System.out.println(l1);
        System.out.println(l2);
        //float
        float pi=-3.14f;
        System.out.println(pi);
        //double
        double PI=-3.141592;
        System.out.println(PI);

        //char
        char c = 'B';
        System.out.println(c);

        // Non primitive Data Types
        // String  name -> "pramod" -, number or char or bunch of char

        String name = "babita";
        String xyz = "BP";
        String pqr = "B";
        System.out.println(name);
        System.out.println(xyz);
        System.out.println(pqr);
    }
}
