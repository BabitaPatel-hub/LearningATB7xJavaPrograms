package Exercise_13072024;

public class Lab026 {
    public static void main(String[] args) {
        //Compound assignment operators
        // +=, -=, *=, /=, %=

        int a = 10;

        System.out.println(a+=15);      //a=a+15
        System.out.println(a-=15);      //a=a-15
        System.out.println(a*=15);      //a=a*15
        System.out.println(a/=15);      //a=a/15
        System.out.println(a%=15);      //a=a%15

    }
}
