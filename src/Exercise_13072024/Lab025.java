package Exercise_13072024;

public class Lab025 {
    public static void main(String[] args) {

        //Unary operators - It will have only one operand
        // + : positive
        // - : negative

        int age = +30; //unary plus
        int lambo = -1; //unary minus

        System.out.println(age);
        System.out.println(lambo);

        System.out.println(age+lambo);
    }
}
