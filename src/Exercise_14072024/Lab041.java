package Exercise_14072024;

public class Lab041 {
    public static void main(String[] args) {
        //Increment (++) and Decrement (--) operators

        // Increment operators : pre and post
        //++operand : pre increment : increment value first and then store in the result
        //operand++ : post increment : value is store in the result first and then incremented

        int a = 10;
        System.out.println(++a); // Exp-11, value-11
        System.out.println(a++); // EXP-10, value-11

    }
}
